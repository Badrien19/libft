#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst != NULL && f != NULL)
	{
		while (lst->next != '\0')
		{
			f(lst->content);
			lst = lst->next;
		}
		f(lst->content);
	}
}
